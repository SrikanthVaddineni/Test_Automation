package Pincode; // Declares the package name

public class Pincode { // Declares the class named Pincode

    public static void main(String[] args) { // Main method: entry point of the program
        int pincode; // Declares an integer variable named pincode
        
        pincode = 560037; // Assigns the value 560037 to the variable pincode
        // Printing the first assigned value
        System.out.println(pincode); // Output: 560037
        
        pincode = 560038; // Reassigns the value 560038 to the variable pincode
        // Printing the reassigned value
        System.out.println(pincode); // Output: 560038
    }

}

